require('dotenv').config(); // –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è –∏–∑ .env
const { Telegraf } = require('telegraf'); // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º Telegraf

const bot = new Telegraf(process.env.BOT_TOKEN); // –°–æ–∑–¥–∞—ë–º –±–æ—Ç–∞ —Å —Ç–æ–∫–µ–Ω–æ–º –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è
const GROUP_ID = Number(process.env.ID); // –ü–æ–ª—É—á–∞–µ–º ID –≥—Ä—É–ø–ø—ã –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è –∏ –ø—Ä–∏–≤–æ–¥–∏–º –∫ —á–∏—Å–ª—É

let players = []; // –°–ø–∏—Å–æ–∫ –∏–≥—Ä–æ–∫–æ–≤

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–ø–∏—Å–∫–∞ –∏–≥—Ä–æ–∫–æ–≤
const sendPlayerList = (ctx) => {
    if (players.length > 0) {
        ctx.reply(`üìã –°–ø–∏—Å–æ–∫ –∏–≥—Ä–æ–∫–æ–≤:\n` + players.join('\n'));
    } else {
        ctx.reply('‚ùå –°–ø–∏—Å–æ–∫ –ø—É—Å—Ç.');
    }
};

// –ö–æ–º–∞–Ω–¥–∞ –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è ID —á–∞—Ç–∞
bot.command('id', (ctx) => {
    ctx.reply(`ID —ç—Ç–æ–≥–æ —á–∞—Ç–∞: ${ctx.chat.id}`); // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º ID —Ç–µ–∫—É—â–µ–≥–æ —á–∞—Ç–∞
});

// –û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π
bot.on('text', (ctx) => {
    const chatId = ctx.chat.id; // –ü–æ–ª—É—á–∞–µ–º ID —á–∞—Ç–∞, –∏–∑ –∫–æ—Ç–æ—Ä–æ–≥–æ –ø—Ä–∏—à–ª–æ —Å–æ–æ–±—â–µ–Ω–∏–µ
    const text = ctx.message.text ? ctx.message.text.trim() : ''; // –ü–æ–ª—É—á–∞–µ–º –∏ –æ—á–∏—â–∞–µ–º —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è

    if (chatId !== GROUP_ID) return; // –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏—è, –µ—Å–ª–∏ –æ–Ω–∏ –Ω–µ –∏–∑ –Ω—É–∂–Ω–æ–π –≥—Ä—É–ø–ø—ã

    if (text.startsWith('+')) { // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è –ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å "+"
        const username = ctx.message.from.username ? `@${ctx.message.from.username}` : ctx.message.from.first_name; // –ü–æ–ª—É—á–∞–µ–º –Ω–∏–∫–Ω–µ–π–º –∏–ª–∏ –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        if (username) {
            players.push(username); // –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ —Å–ø–∏—Å–æ–∫
            ctx.reply(`‚úÖ ${username} –¥–æ–±–∞–≤–ª–µ–Ω –≤ —Å–ø–∏—Å–æ–∫!`); // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ
            sendPlayerList(ctx); // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ–±–Ω–æ–≤–ª—ë–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ –∏–≥—Ä–æ–∫–æ–≤
        } else {
            ctx.reply('‚ö†Ô∏è –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞.'); // –°–æ–æ–±—â–∞–µ–º –æ–± –æ—à–∏–±–∫–µ
        }
    } else if (text === '/list') { // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–∞ –ª–∏ –∫–æ–º–∞–Ω–¥–∞ /list
        sendPlayerList(ctx); // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–ø–∏—Å–æ–∫ –∏–≥—Ä–æ–∫–æ–≤
    } else if (text === '/clear') { // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–∞ –ª–∏ –∫–æ–º–∞–Ω–¥–∞ /clear
        players = []; // –û—á–∏—â–∞–µ–º —Å–ø–∏—Å–æ–∫ –∏–≥—Ä–æ–∫–æ–≤
        ctx.reply('üóëÔ∏è –°–ø–∏—Å–æ–∫ –æ—á–∏—â–µ–Ω!'); // –ü–æ–¥—Ç–≤–µ—Ä–∂–¥–∞–µ–º –æ—á–∏—Å—Ç–∫—É
        sendPlayerList(ctx); // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫
    }
});

bot.launch(); // –ó–∞–ø—É—Å–∫–∞–µ–º –±–æ—Ç–∞
